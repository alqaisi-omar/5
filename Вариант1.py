# Получение текста от пользователя
text = input("Введите русский текст: ")

# Разделение текста на слова с использованием пробела в качестве разделителя
words = text.split()

# Инициализация счетчика для подсчета слов, начинающихся с буквы "е"
count = 0

# Проверка слов, начинающихся с буквы "е"
for word in words:
    # Приведение слова к нижнему регистру для сравнения без учета регистра
    if word.lower().startswith('е'):
        count += 1

# Вывод количества слов, начинающихся с буквы "е"
print("Количество слов, начинающихся с буквы 'е':", count)
