# Получение строки от пользователя
string = input("Введите строку: ")

# Поиск индексов первой открывающейся и первой закрывающейся скобок
open_index = string.find('(')
close_index = string.find(')')

# Если обе скобки найдены
if open_index != -1 and close_index != -1:
    # Вывод символов, расположенных между скобками
    print("Символы между первой открывающейся и первой закрывающейся скобками:")
    print(string[open_index + 1: close_index])
else:
    print("Ошибка: В строке нет открывающейся или закрывающейся скобки.")
